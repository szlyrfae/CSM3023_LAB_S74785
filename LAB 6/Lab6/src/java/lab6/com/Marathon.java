package lab6.com;

public class Marathon {
    private String runnerName;
    private String runnerIC;
    private String category;
    private String tShirtSize;

    // Getters and Setters
    public String getRunnerName() { return runnerName; }
    public void setRunnerName(String runnerName) { this.runnerName = runnerName; }

    public String getRunnerIC() { return runnerIC; }
    public void setRunnerIC(String runnerIC) { this.runnerIC = runnerIC; }

    public String getCategory() { return category; }
    public void setCategory(String category) { this.category = category; }

    public String getTShirtSize() { return tShirtSize; }
    public void setTShirtSize(String tShirtSize) { this.tShirtSize = tShirtSize; }
}